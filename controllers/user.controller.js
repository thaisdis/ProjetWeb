const UserModel = require("../models/user.model"); //appel a la base de données 
const ObjectID = require("mongoose").Types.ObjectId; // pr que ID soient reconnus par la base de données
//achats ici? 
module.exports.getAllUsers = async (req, res) => { // en fiche? 
  const users = await UserModel.find().select("-password"); // select = tu prend tout, sans le mot de passe en front
  res.status(200).json(users); // on fait transiter la data 
};

module.exports.userInfo = (req, res) => {
  if (!ObjectID.isValid(req.params.id))
    return res.status(400).send("ID unknown : " + req.params.id);

  UserModel.findById(req.params.id, (err, docs) => {
    if (!err) res.send(docs);
    else console.log("ID unknown : " + err);
  }).select("-password"); // je ne veux pas que le password transite en front 
};

module.exports.updateUser = async (req, res) => {
  if (!ObjectID.isValid(req.params.id))
    return res.status(400).send("ID unknown : " + req.params.id);

  try {
    await UserModel.findOneAndUpdate(
      { _id: req.params.id }, // req.param= param dans l'url
      {
        $set: {
          bio: req.body.bio, // req.body = input
        },
      },
      { new: true, upsert: true, setDefaultsOnInsert: true }, // paramètres obligatoires si on fait un put
      (err, docs) => {
        if (!err) return res.send(docs);
        if (err) return res.status(500).send({ message: err });
      }
    );
  } catch (err) {
    return res.status(500).json({ message: err });
  }
};

module.exports.deleteUser = async (req, res) => { // la flèche
  if (!ObjectID.isValid(req.params.id)) // si on passe le bon bail en param
    return res.status(400).send("ID unknown : " + req.params.id); //status 400? 

  try {
    await UserModel.remove({ _id: req.params.id }).exec();
    res.status(200).json({ message: "Successfully deleted. " }); //communication back/front
  } catch (err) {
    return res.status(500).json({ message: err });
  }
};

module.exports.follow = async (req, res) => {
  if (
    !ObjectID.isValid(req.params.id) ||
    !ObjectID.isValid(req.body.idToFollow)
  )
    return res.status(400).send("ID unknown : " + req.params.id);

  try {
    // add to the follower list
    await UserModel.findByIdAndUpdate(
      req.params.id,
      { $addToSet: { following: req.body.idToFollow } },
      { new: true, upsert: true },
      (err, docs) => {
        if (!err) res.status(201).json(docs);
        else return res.status(400).jsos(err);
      }
    );
    // add to following list
    await UserModel.findByIdAndUpdate(
      req.body.idToFollow,
      { $addToSet: { followers: req.params.id } },
      { new: true, upsert: true },
      (err, docs) => {
        // if (!err) res.status(201).json(docs);
        if (err) return res.status(400).jsos(err);
      }
    );
  } catch (err) {
    return res.status(500).json({ message: err });
  }
};

